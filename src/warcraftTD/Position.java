package warcraftTD;

public class Position {
	double x;
	double y;
	
	/**
	 * Classe qui permet d'avoir la position sur l'axe des x et des y des monstres et des tours
	 * @param x
	 * @param y
	 */
	public Position(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	public Position(Position p) {
		x = p.x;
		y = p.y;
	}
	
	public boolean equals(Position p) {
		return x == p.x && y == p.y;
	}
	
	/**
	 * Mesure la distance euclidienne entre 2 positions.
	 * @param p
	 * @return
	 */
	public double dist(Position p) {
		return Math.sqrt(Math.pow(x - p.x, 2) + Math.pow(y - p.y, 2));
	}

	/**
	 * Retourne la position du point sur l'axe des x et des y
	 */
	public String toString() {
		return "(" + Double.toString(x) + "," + Double.toString(y) + ")";
	}
	
	/*
	 * ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
	 */
	public static double minX(Position p1,Position p2) 
	{
		if(p1.x <= p2.x) return p1.x;
		else return p2.x;		
	}
	
	public static double maxX(Position p1,Position p2) 
	{
		if(p1.x > p2.x) return p1.x;
		else return p2.x;		
	}
	/*
	 * ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
	 */
	
	public static double maxY(Position p1,Position p2) 
	{
		if(p1.y >= p2.y) return p1.y;
		else return p2.y;		
	}
	
	public static double minY(Position p1,Position p2) 
	{
		if(p1.y < p2.y) return p1.y;
		else return p2.y;		
	}
}
